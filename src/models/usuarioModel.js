function buscarPersonagem(idUsuario) {
    var instrucaoSql = `SELECT p.nome 
    FROM personagem p 
    JOIN usuario u ON u.fk_personagem = p.id_personagem 
    WHERE u.id_usuario = ${idUsuario};`;
    return database.executar(instrucaoSql);
}

